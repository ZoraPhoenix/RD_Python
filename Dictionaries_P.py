# Dictionaries

# A dictionary is a collection of key-value pairs. Each key is unique and maps to a value.
phonebook = {}
phonebook["Zora"] = "123-456-7890"
print(phonebook["Zora"])

# Let's add a few more entries to the phonebook
phonebook["Robert"] = "234-567-8901"
phonebook["Alice"] = "345-678-9012"
phonebook["Bob"] = "456-789-0123"

print(phonebook)

